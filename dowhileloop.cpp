#include<bits/stdc++.h>   // Includes all standard C++ libraries
using namespace std;

int main () {
    int i = 2;                     // Initialize counter i with value 2
    do {
        cout << "\nHello World";   // Print "Hello World" once
        i = i + 1;                 // Increment i (now i = 3)
    } while(i <= 1);               // Condition is false, so loop ends after first run
    return 0;
}
